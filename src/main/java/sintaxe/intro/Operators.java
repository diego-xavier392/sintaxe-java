package sintaxe.intro;

public class Operators {
    public static void main(String[] args) {
        int x = 100 + 250;         // 350 ( x = 100 + 250)
        int sum1 = 100 + 250;      // 350 ( 100 + 250 )
        int sum2 = sum1 + 250;     // 600 ( 350 + 250 )
        int sum3 = sum2 + sum2;    // 1200 ( 600 + 600)


    }
}
