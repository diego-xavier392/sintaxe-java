package sintaxe.variaveis;

public class NumeroComVirgula {
    public static void main(String[] args) {

    float compraMercado = 56.4f;
    float faturaCartao = 100.56f;
    float valorCurso = 259.99f;

    System.out.println("compra do Mercado: " + compraMercado);
    System.out.println("fatura do Cartão: " + faturaCartao);
    System.out.println("valor do Curso" + valorCurso);


    //não precisamos usar o F usando double

    double tipo1 = 555.45;
    double tipo2 = 489.75;
    double tipo3 = 523.63;

    System.out.println("tipo 1= " + tipo1);
    System.out.println("tipo 2= " + tipo2);
    System.out.println("tipo 3= " + tipo3);

    }
}
